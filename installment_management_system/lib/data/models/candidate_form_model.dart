import '../../domain/entities/candidate_form.dart';

class CandidateFormModel extends CandidateForm {
  CandidateFormModel({
    required super.id,
    required super.userId,
    required super.serialNo,
    required super.title,
    required super.firstName,
    required super.lastName,
    required super.initial,
    required super.email,
    required super.fatherName,
    required super.dob,
    required super.gender,
    required super.profession,
    required super.mailingStreet,
    required super.mailingCity,
    required super.mailingPostal,
    required super.mailingCountry,
    required super.serviceProvider,
    required super.fileNo,
    required super.referenceNo,
    required super.simNo,
    required super.imsi1,
    required super.imsi2,
    required super.typeOfPlan,
    required super.creditCardType,
    required super.contractValue,
    required super.dateOfIssue,
    required super.dateOfRenewal,
    required super.installment,
    required super.amountInWords,
    required super.remarks,
    super.score,
    super.mistakes,
    super.status,
  });

  factory CandidateFormModel.fromJson(Map<String, dynamic> json) {
    return CandidateFormModel(
      id: json['id']?.toString() ?? '',
      userId: json['userId']?.toString() ?? '',
      serialNo: json['serialNo']?.toString() ?? '',
      title: json['title']?.toString() ?? '',
      firstName: json['firstName']?.toString() ?? '',
      lastName: json['lastName']?.toString() ?? '',
      initial: json['initial']?.toString() ?? '',
      email: json['email']?.toString() ?? '',
      fatherName: json['fatherName']?.toString() ?? '',
      dob: json['dob']?.toString() ?? '',
      gender: json['gender']?.toString() ?? '',
      profession: json['profession']?.toString() ?? '',
      mailingStreet: json['mailingStreet']?.toString() ?? '',
      mailingCity: json['mailingCity']?.toString() ?? '',
      mailingPostal: json['mailingPostal']?.toString() ?? '',
      mailingCountry: json['mailingCountry']?.toString() ?? '',
      serviceProvider: json['serviceProvider']?.toString() ?? '',
      fileNo: json['fileNo']?.toString() ?? '',
      referenceNo: json['referenceNo']?.toString() ?? '',
      simNo: json['simNo']?.toString() ?? '',
      imsi1: json['imsi1']?.toString() ?? '',
      imsi2: json['imsi2']?.toString() ?? '',
      typeOfPlan: json['typeOfPlan']?.toString() ?? '',
      creditCardType: json['creditCardType']?.toString() ?? '',
      contractValue: json['contractValue']?.toString() ?? '',
      dateOfIssue: json['dateOfIssue']?.toString() ?? '',
      dateOfRenewal: json['dateOfRenewal']?.toString() ?? '',
      installment: json['installment']?.toString() ?? '',
      amountInWords: json['amountInWords']?.toString() ?? '',
      remarks: json['remarks']?.toString() ?? '',
      score: json['score'] != null ? (json['score'] as num).toDouble() : 0.0,
      mistakes: json['mistakes']?.toString() ?? '[]',
      status: json['status']?.toString() ?? 'pending',
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'userId': userId,
      'serialNo': serialNo,
      'title': title,
      'firstName': firstName,
      'lastName': lastName,
      'initial': initial,
      'email': email,
      'fatherName': fatherName,
      'dob': dob,
      'gender': gender,
      'profession': profession,
      'mailingStreet': mailingStreet,
      'mailingCity': mailingCity,
      'mailingPostal': mailingPostal,
      'mailingCountry': mailingCountry,
      'serviceProvider': serviceProvider,
      'fileNo': fileNo,
      'referenceNo': referenceNo,
      'simNo': simNo,
      'imsi1': imsi1,
      'imsi2': imsi2,
      'typeOfPlan': typeOfPlan,
      'creditCardType': creditCardType,
      'contractValue': contractValue,
      'dateOfIssue': dateOfIssue,
      'dateOfRenewal': dateOfRenewal,
      'installment': installment,
      'amountInWords': amountInWords,
      'remarks': remarks,
      'score': score,
      'mistakes': mistakes,
      'status': status,
    };
  }
}
